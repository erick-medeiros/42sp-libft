#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	int		i;
	int		size;

	size = ft_strlen(s);
	dup = (char *) malloc(sizeof(char) * (size + 1));
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
